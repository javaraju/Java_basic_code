package com.AppsPatern.java;

public class Pattern6 {

	public static void main(String[] args) {
		int n=5;
		int k=5;
		for(int i=1;i<=n;i++)
		{
			
			for(int j=1;j<=n;j++)
			{
				if(j<=n)
				{
			       System.out.print(k);
			       
				}
				
			}
			k--;
			System.out.println();
		}


	}

}
